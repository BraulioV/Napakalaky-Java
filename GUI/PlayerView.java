    private void buyLevelsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buyLevelsButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<Treasure> selVisible = getSelectedTreasures(visibleTreasures);
        ArrayList<Treasure> selHidden = getSelectedTreasures(hiddenTreasures);
        playerModel.buyLevels(selVisible, selHidden);
        setPlayer(this.napakalakyModel.getCurrentPlayer());
        
    }//GEN-LAST:event_buyLevelsButtonActionPerformed

    private void discardTreasureButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_discardTreasureButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<Treasure> selVisible = getSelectedTreasures(visibleTreasures);
        ArrayList<Treasure> selHidden = getSelectedTreasures(hiddenTreasures);
        for(Treasure t:selHidden){
            this.playerModel.discardHiddenTreasure(t);
        }
        for(Treasure t:selVisible){
            this.playerModel.discardVisibleTreasure(t);
        }
        this.setPlayer(napakalakyModel.getCurrentPlayer());
    }//GEN-LAST:event_discardTreasureButtonActionPerformed


