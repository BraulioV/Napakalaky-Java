    public void setPlayer(Player p){
        playerModel = p;
        name.setText(this.playerModel.getName());
        level.setText(Integer.toString(this.playerModel.getLevels()));
        if(this.playerModel.isDead())
            dead.setText("Yes");
        else
            dead.setText("No");
        fillTreasursePanel(hiddenTreasures, playerModel.getHiddenTreasures());
        fillTreasursePanel(visibleTreasures, playerModel.getVisibleTreasures());
        cultistView1.setCultist(playerModel);
        repaint();
        revalidate();
    }
    
    public void setNapakalaki(Napakalaki n){
        this.napakalakyModel = n;
    }
    
    public void fillTreasursePanel(JPanel aPanel, ArrayList<Treasure> aList){
        // Eliminar informaci칩n antigua
        aPanel.removeAll();
        // Se recorren los tesoros y se a침aden sus vistas al panel
        for(Treasure t:aList){
            TreasureView aTreasureView = new TreasureView();
            aTreasureView.setTresaure(t);
            aTreasureView.setVisible(true);
            aPanel.add(aTreasureView);
        }
        // se fuerza la actualizaci칩n visual del panel
        
        aPanel.repaint();
        aPanel.revalidate();
    }
    
    public ArrayList<Treasure> getSelectedTreasures(JPanel aPanel){
        // Se recorren los tesoros que contiene el panel,
        // almacenando en un vector aquellosque est치n seleccionados.
        // Finalmente se devuelve dicho vector
        
        TreasureView tv;
        ArrayList<Treasure> output = new ArrayList();
        for(Component c:aPanel.getComponents()){
            tv = (TreasureView)c;
            if(tv.isSelected())
                output.add(tv.getTreasure());
        }
        
        return output;
    }
    
    
